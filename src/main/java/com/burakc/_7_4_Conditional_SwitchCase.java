package com.burakc;

public class _7_4_Conditional_SwitchCase {
    static void main() {
        int number = 6;
        switch (number){
            case 1:
                System.out.println("Sayı Birdir");
                break;
            case 2:
                System.out.println("Sayı İki");
                break;
            case 3:
                System.out.println("Sayı Üç");
                break;
            case 4:
                System.out.println("Sayı Dört");
                break;
            case 5:
                System.out.println("Sayı Beş");
                break;
            case 6:
                System.out.println("Sayı Altı");
                break;
        }
    }
}
