package com.burakc.tutorial;

public class _7_3_Conditional_Multiple {
    static void main() {
        int number =5;

        if (number ==1){
            System.out.println("Sayısı Birdir");
        }else if (number ==2){
            System.out.println("Sayısı İki");
        }else if (number ==3){
            System.out.println("Sayısı Üç");
        }else if (number ==4){
            System.out.println("Sayısı Dört");
        }else if (number ==5){
            System.out.println("Sayısı Beş");
        }else if (number ==6){
            System.out.println("Sayısı Altı");
        }else {
            System.out.println("1<=SAYI<==6 dışındadır");
        }

    }
}
