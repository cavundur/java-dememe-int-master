package com.burakc.tutorial;

public class _4_Operators {
    static void main () {

        int number1 = 43;
        int number2 = 5;

        System.out.println("Toplam :" + (number1+number2));
        System.out.println("Çıkartma :" + (number1-number2));
        System.out.println("Çarpma :" + (number1*number2));
        System.out.println("Bölme :" + (number1/number2));
        System.out.println("Kalan :" + (number1%number2));
    }
}

